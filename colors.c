#include "fractol.h"

unsigned char palette[48] = {
    66, 30, 15,
    25, 7, 26,
    9, 1, 47,
    4, 4, 73,
    0, 7, 100,
    12, 44, 138,
    24, 82, 177,
    57, 125, 209,
    134, 181, 229,
    211, 236, 248,
    241, 233, 191,
    248, 201, 95,
    255, 170, 0,
    204, 128, 0,
    153, 87, 0,
    106, 52, 3
};

int	get_color(int m, t_env *env)
{
	unsigned char * p = palette+(m&(15))*3;
	int r = p[0];
	int g = p[1];
	int b = p[2];
	return (m == env->iterations ? 0 : (r<<16|g<<8|b));
}
